function getDescriptionOfPerson(person) {
  // console.log(person.name +" is a "+person.occupation+
  // " from "+person.birthPlace+"."
  // )
  return (
    person.name +
    ' is a ' +
    person.occupation +
    ' from ' +
    person.birthPlace +
    '.'
  );
}
